function activarModoOscuro() {
    document.body.style.backgroundColor = "black";
    document.body.style.color = "white"; // Cambia el color del texto en modo oscuro
}

function activarModoClaro() {
    document.body.style.backgroundColor = "#F2E4D8"; // Color de fondo claro
    document.body.style.color = "black"; // Cambia el color del texto en modo claro
}
